package com.selenium.methodoverloadind_overriding;

// String Argument main method will be always execute first.


public class Method_Overloading4 {
	
	public static void main(String[] args) {
		System.out.println("Main Method - String Argument");
		}
		
		
		public static void main(int[] args) {
			System.out.println("Main Method - Int Argument");
			}
		
		
		public static void main(float[] args) {
			System.out.println("Main Method - Float Argument");
			}
		
		
		public static void main(double[] args) {
			System.out.println("Main Method - Double Argument");
			}
		
		
		public static void main(char[] args) {
			System.out.println("Main Method - Char Argument");
			}
		
		
	
}
